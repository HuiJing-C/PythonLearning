if __name__ == '__main__':
    # 绝对值
    print(abs(-1))
    # 转整数
    print(int("123"))
    # 转浮点数
    print(float("123.567"))
    # 最大值
    print(max(1, 2, 34, 5, 6))
    # 转字符串
    print(str(123))
    # 转布尔
    print(bool("True"))
    # 函数赋值
    a = abs
    print(a(-1))
    # 转16进制
    b = hex
    print(b(8))
